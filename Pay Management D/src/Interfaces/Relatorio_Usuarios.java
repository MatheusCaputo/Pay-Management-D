    public void listarUsuarios () throws SQLException{
        try{
        String sql = "select* from relatorio_usuarios";
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();
        jTableUsuario.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            con.close();
            pst.close();
            rs.close();
        }
    }
    

